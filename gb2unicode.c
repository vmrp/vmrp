//编码转换，从GB2312转成unicode
const unsigned char *GBCodeToUnicode(unsigned char *gbCode) {
    int i=0;

    if ((*(gbCode + 1) >= 0xa1) && (*(gbCode + 1) <= 0xfe)) {
        if ((*gbCode >= 0xa1) && (*gbCode <= 0xa9)) {
            i = ((*gbCode - 0xa1) * 94 + (*(gbCode + 1) - 0xa1)) * 2;
        } else if ((*gbCode >= 0xb0) && (*gbCode <= 0xf7)) {
            i = ((*gbCode - 0xb0 + 9) * 94 + (*(gbCode + 1) - 0xa1)) * 2;
        }
        if (i < sizeof(gb2uTable)) {
            return &gb2uTable[i];
        }
    }
    return (void *)0;
}

char *gbToUCS2BE(uint8_t *gbCode, uint32_t *outSize) {
    int bufSize = strlen((char*)gbCode) * 2 + 2;
    char *unicode = malloc(bufSize);

    if (outSize) *outSize = bufSize;

    int i = 0, j = 0;

    while (gbCode[i] && j < bufSize - 2) {
        if (gbCode[i] < 0x80) {
            unicode[j] = 0;
            unicode[j + 1] = gbCode[i];
            i += 1;
        } else {
            const unsigned char *pReturn = GBCodeToUnicode(&gbCode[i]);
            if (pReturn) {
                unicode[j] = *(pReturn + 1);
                unicode[j + 1] = *pReturn;
            } else {  // 全角空格
                unicode[j] = 0x30;
                unicode[j + 1] = 0x00;
            }
            i += 2;
        }
        j += 2;
    }
    unicode[j] = 0;
    unicode[j + 1] = 0;
    return unicode;
}



